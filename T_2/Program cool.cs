void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row -1, col); // направление вверх
        FillImage(row, col - 1); // влево
        FillImage(row + 1, col); // вниз
        FillImage(row, col + 1); // вправо

    }
}
